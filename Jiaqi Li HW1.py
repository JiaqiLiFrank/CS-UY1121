def print_course_grades(original_lst):
 result_lst = []
 for students in original_lst:
  course_grade_lst = []
  for index1 in range(4,7):
   quiz_score = (students[index1][1][0][0]+students[index1][1][0][1]+students[index1][1][0][2])/300*10
   test1_score = students[index1][1][2][0]/100*20
   test2_score = students[index1][1][2][1]/100*25
   test3_score = students[index1][1][2][2]/100*30
   test_score = test1_score + test2_score + test3_score
   hw_sort_lst = students[index1][1][1]
   hw_sort_lst.sort()
   hw_sort_lst.pop(0)
   intermediate = 0
   for numbers in hw_sort_lst:
    intermediate += numbers
   hw_score = intermediate/600*15
   course_grade = quiz_score + test_score + hw_score
   single_course_lst = []
   single_course_lst.append(str(course_grade))
   single_course_lst.append(students[index1][0])
   course_grade_lst.append(single_course_lst)
   course_grade_lst.sort(reverse=True)
   single_course_lst.insert(0,str(single_course_lst[1]))
   single_course_lst.pop(2)
  name_lst = []
  name_lst.append(students[0]+ " " + students[1])
  course_grade_lst.insert(0," ".join(name_lst))
  result_lst.append(course_grade_lst)
 return(result_lst)

